// Create a 4 function add, substract, multiply and divide

function sum(num1, num2){
let result = num1 + num2;
return result
}
f1 = sum (3,7)
console.log(f1)

function sub(num1, num2) {
    let result = num1 - num2;
    return result
}
f2 = sub (8,5)
console.log(f2)

function mul(num1, num2) {
    let result = num1 * num2;
    return result
}
f3 = mul(8,9)
console.log(f3)

function div (num1, num2){
let result = num1 / num2;
return result
}
f4 = div(10,2)
console.log(f4)
